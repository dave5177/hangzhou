	public void logic() {

		roadFactory.createMap();// 生产地图

		// ///////////////游戏内容逻辑//////////////////
		baby.logic();
		for (int i = sizeMapElV - 1; i >= 0; i--) {
			((MapElements) v_MapEl.elementAt(i)).logic();
		}

		for (int i = v_gold.size() - 1; i >= 0; i--) {
			((Gold) v_gold.elementAt(i)).logic();
		}
		for (int i = v_missile.size() - 1; i >= 0; i--) {
			((Missile) v_missile.elementAt(i)).logic();
		}
//		for (int i = v_futi.size() - 1; i >= 0; i--) {
//			((FuTi) v_futi.elementAt(i)).logic();
//		}

		runDistance += speed_run;

		if (fuTi) {// 扶梯时间控制
			fuTiTime++;
			if (fuTiTime > fuTiTimeMax) {
				fuTiTime = 0;
				fuTiTimeMax = 0;
				fuTi = false;
			}
		}

		if (attract) {// 吸铁石时间控制
			attractTime++;
			if (attractTime > attractTimeMax) {
				attractTime = 0;
				attractTimeMax = 0;
				attract = false;
			}
		}

		// /////////////////背景移动//////////////////
		x_back -= speed_run / 4;
		if (x_back <= -640) {
			x_back += 640;
		}
		// if (x_back <= -64) {
		// x_back = 0;
		// backImgStarIndex++;
		// if (backImgStarIndex > 9)
		// backImgStarIndex = 0;
		// }

		speedCtrl();
	}

	/**
	 * 速度控制
	 */
	private void speedCtrl() {
		if (speed_run < speedMax)
			speed_run++;
		else if (speed_run > speedMax)
			speed_run--;
	}

