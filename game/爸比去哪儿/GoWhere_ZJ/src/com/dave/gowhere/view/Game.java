	/**
	 * 进入浮梯效果
	 */
	public void goIntoFuTi(int fuTiTime) {
		MapElements lastMapEl = null;
		MapElements mapEl = null;

		if (sizeMapElV > 0) {
			int firstMapElX = ((MapElements) v_MapEl.elementAt(0)).x;
			int firstMapElY = ((MapElements) v_MapEl.elementAt(0)).y;
//			int endMapElX = ((MapElements) v_MapEl.elementAt(sizeMapElV - 1)).x;
//			int endMapElY = ((MapElements) v_MapEl.elementAt(sizeMapElV - 1)).y;
			if (firstMapElX > -20) {
				int initalNum = (firstMapElX + 100) / 80;
				for (int i = 0; i < initalNum; i++) {
//					v_futi.addElement(new FuTi(this, firstMapElX
//							- (initalNum - i) * 80, firstMapElY, a_img_futi));
					addMapEl(new FuTi(this, firstMapElX
							- (initalNum - i) * 80, firstMapElY, a_img_futi), i);
				}
			}
			for (int i = 0; i < sizeMapElV; i++) {
				mapEl = (MapElements) v_MapEl.elementAt(i);
				if (mapEl.getType() == MapElements.TYPE_ROAD
						|| mapEl.getType() == MapElements.TYPE_SPRING) {//获得道路或弹簧
					if (lastMapEl == null)
						lastMapEl = (MapElements) v_MapEl.elementAt(i);

					if (mapEl.x - lastMapEl.x >= 160) {
						int num = (mapEl.x - lastMapEl.x - 80) / 80;
						for (int j = num - 1; j >= 0; j--) {
//							v_futi.addElement(new FuTi(this, lastMapEl.x
//									+ (j + 1) * 80, lastMapEl.y, a_img_futi));
							addMapEl(new FuTi(this, lastMapEl.x
									+ (j + 1) * 80, lastMapEl.y, a_img_futi), i);
						}
					}

					lastMapEl = mapEl;
				} else if(mapEl.getType() == MapElements.TYPE_MONSTERAIR
						|| mapEl.getType() == MapElements.TYPE_MONSTERAIR) {//将怪物移除掉
					removeMapEl(mapEl);
				}

				//
				// if(mapEl.getType() == MapElements.TYPE_MONSTERAIR ||
				// mapEl.getType() == MapElements.TYPE_THORN) {
				//
				// }
			}
			
			int endMapElX = lastMapEl.x;
			int endMapElY = lastMapEl.y;
			
			if(940 - endMapElX > 80) {
				int num = (940 - endMapElX) / 80;
				for (int j = num - 1; j >= 0; j--) {
//						v_futi.addElement(new FuTi(this, lastMapEl.x
//								+ (j + 1) * 80, lastMapEl.y, a_img_futi));
					addMapEl(new FuTi(this, endMapElX
							+ (j + 1) * 80, endMapElY, a_img_futi));
				}
			}
		} else {
			for (int i = 0; i < 12; i++) {
				addMapEl(new FuTi(
						this, 60 + i * 80, 350, a_img_futi));
			}
		}
		

		fuTi = true;
		fuTiTimeMax += fuTiTime;
	}

