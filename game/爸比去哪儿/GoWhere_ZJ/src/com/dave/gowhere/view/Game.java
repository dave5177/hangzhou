	/**
	 * 移除地图上的元素
	 * 
	 * @param mapEl
	 */
	public void removeMapEl(MapElements mapEl) {
		v_MapEl.removeElement(mapEl);
		sizeMapElV--;
	}

	/**
	 * 添加一个元素至末尾
	 * 
	 * @param mapEl
	 */
	public void addMapEl(MapElements mapEl) {
		v_MapEl.addElement(mapEl);
		sizeMapElV++;
	}

	/**
	 * 添加一个元素到指定位置
	 * 
	 * @param mapEl
	 * @param index
	 */
	public void addMapEl(MapElements mapEl, int index) {
		Vector vSave = new Vector();
		for (int j = sizeMapElV - 1; j >= index; j--) {// 后面的先保存到另一个容器
			vSave.addElement(v_MapEl.elementAt(j));
			v_MapEl.removeElementAt(j);
		}
		v_MapEl.addElement(mapEl);// 添加目标元素
		int sizeSave = vSave.size();
		for (int i = sizeSave - 1; i >= 0; i--) {// 添加原先保存的元素
			v_MapEl.addElement(vSave.elementAt(i));
		}
		sizeMapElV++;
	}

